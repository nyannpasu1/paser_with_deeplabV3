class DeepLabV3Model(nn.Module):
    def __init__(self, n_channels, n_classes, backbone_name='resnet50', output_stride=8, pretrained_backbone=True, **kwargs):
        super(DeepLabV3Model, self).__init__()
        # The original _load_model uses pretrained_backbone. We pass it from config.
